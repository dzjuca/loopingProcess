function [emulsion, C_g_e] = bc_dp_ge_Fcn(u, Global)
% -------------------------------------------------------------------------

    id_g_e  = 'gas_emulsion';
    [g1e, g2e] = assignValuesFcn(u, Global, id_g_e);

% -------------------------------------------------------------------------

    g1e(1) = Global.streamGas.composition.O2; 
    g2e(1) = Global.streamGas.composition.N2; 

% -------------------------------------------------------------------------

    emulsion.g1e = g1e; 
    emulsion.g2e = g2e; 

% -------------------------------------------------------------------------

    C_g_e = [g1e, g2e];

% -------------------------------------------------------------------------
end